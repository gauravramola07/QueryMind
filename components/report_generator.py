def _hf(canv, doc, fi_name):
    """Draw header + footer on canvas. Called for every page."""
    canv.saveState()
    # Background
    canv.setFillColor(C_BG)
    canv.rect(0, 0, PW, PH, fill=1, stroke=0)
    # Top bar
    canv.setFillColor(C_PURPLE)
    canv.rect(0, PH - 3, PW, 3, fill=1, stroke=0)
    # Header text
    canv.setFont('Helvetica', 7.5)
    canv.setFillColor(C_MUTED)
    canv.drawString(LM, PH - 1.35*cm, 'QueryMind Analytics Report')
    canv.drawRightString(PW - RM, PH - 1.35*cm, fi_name)
    # Header rule
    canv.setStrokeColor(C_BORDER)
    canv.setLineWidth(0.4)
    canv.line(LM, PH - 1.5*cm, PW - RM, PH - 1.5*cm)
    # Footer rule + text - position well below content area
    footer_y = BM + 0.3*cm  # Positioned within margin, above content
    canv.line(LM, footer_y, PW - RM, footer_y)
    canv.setFont('Helvetica-Oblique', 7.5)
    ts = datetime.now().strftime('%B %d, %Y  %H:%M')
    canv.drawString(LM, footer_y + 0.2*cm, f'Generated by QueryMind  |  {ts}')
    canv.drawRightString(PW - RM, footer_y + 0.2*cm, f'Page {doc.page}')
    canv.restoreState()


def _draw_cover_content(canv, fi, health):
    """
    Draw Page 1 body content directly on canvas.
    Called from onFirstPage AFTER _hf().
    All coordinates are absolute from bottom-left of page.
    """
    canv.saveState()

    # Start below the header with proper vertical padding
    y = PH - 4.5 * cm   # ≈ 772 pts - proper distance from header

    # ══ BRAND NAME ═══════════════════════════════════════════════════════════
    canv.setFont('Helvetica-Bold', 36)
    canv.setFillColor(C_WHITE)
    canv.drawString(LM, y, 'QueryMind')
    y -= 0.42 * cm

    # ── Purple accent line ────────────────────────────────────────────────────
    canv.setStrokeColor(C_PURPLE)
    canv.setLineWidth(1.5)
    canv.line(LM, y, PW - RM, y)
    y -= 0.52 * cm

    # ══ SUBTITLE ═════════════════════════════════════════════════════════════
    canv.setFont('Helvetica', 13)
    canv.setFillColor(C_PURPLT)
    canv.drawString(LM, y, 'Data Analytics Report')
    y -= 0.9 * cm

    # ══ METADATA ═════════════════════════════════════════════════════════════
    now  = datetime.now().strftime('%B %d, %Y  %H:%M')
    meta = [
        ('Dataset',   fi.get('file_name', '—')),
        ('Rows',      f"{fi.get('num_rows', 0):,}"),
        ('Columns',   str(fi.get('num_cols', 0))),
        ('Generated', now),
    ]
    vx = LM + 2.3 * cm
    for label, val in meta:
        canv.setFont('Helvetica-Bold', 9.5)
        canv.setFillColor(C_PURPLT)
        canv.drawString(LM, y, f'{label}:')
        canv.setFont('Helvetica', 9.5)
        canv.setFillColor(C_WHITE)
        canv.drawString(vx, y, val)
        y -= 0.52 * cm

    y -= 0.5 * cm  # gap before quality section

    # ══ DATA QUALITY HEADING ══════════════════════════════════════════════════
    canv.setFont('Helvetica-Bold', 13)
    canv.setFillColor(C_PURPLT)
    canv.drawString(LM, y, 'Data Quality')
    y -= 0.55 * cm

    # ══ GRADE PILL ════════════════════════════════════════════════════════════
    grade     = str(health.get('grade', 'C')).upper()
    grade_c   = _gc(grade)
    grade_lbl = health.get('label', 'Fair')
    overall   = health.get('overall', 0)

    pill_h = 1.75 * cm
    pill_y = y - pill_h

    # Card background
    canv.setFillColor(C_CARD)
    canv.roundRect(LM, pill_y, UW, pill_h, 5, fill=1, stroke=0)
    # Coloured border
    canv.setStrokeColor(grade_c)
    canv.setLineWidth(1.0)
    canv.roundRect(LM, pill_y, UW, pill_h, 5, fill=0, stroke=1)

    # Grade letter — left cell, perfectly centred
    cell_w   = 2.8 * cm
    cell_cx  = LM + cell_w / 2
    cell_mid = pill_y + pill_h / 2
    # Helvetica-Bold 40pt: cap-height ≈ 28pt → optical centre offset = 14pt
    canv.setFont('Helvetica-Bold', 40)
    canv.setFillColor(grade_c)
    canv.drawCentredString(cell_cx, cell_mid - 14, grade)

    # Divider
    canv.setStrokeColor(C_BORDER)
    canv.setLineWidth(0.5)
    canv.line(LM + cell_w, pill_y + 6, LM + cell_w, pill_y + pill_h - 6)

    # Grade label + score — right of divider
    tx = LM + cell_w + 0.45 * cm
    canv.setFont('Helvetica-Bold', 15)
    canv.setFillColor(grade_c)
    canv.drawString(tx, cell_mid + 2, grade_lbl)
    canv.setFont('Helvetica', 9)
    canv.setFillColor(C_MUTED)
    canv.drawString(tx, cell_mid - 14, f'Overall Score: {overall}/100')

    y = pill_y - 0.3 * cm

    # ══ PROGRESS BARS ════════════════════════════════════════════════════════
    breakdown = health.get('breakdown', {})
    if breakdown:
        label_w  = 3.0 * cm
        score_w  = 2.2 * cm
        bar_w    = UW - label_w - score_w
        bar_x    = LM + label_w
        score_x  = bar_x + bar_w + 0.15 * cm
        row_h    = 0.75 * cm
        n        = len(breakdown)
        tbl_h    = n * row_h

        # Cap table height to prevent footer overlap
        footer_y = BM + 0.5 * cm
        max_height = (PH - 4.5 * cm) - footer_y
        if tbl_h > max_height:
            row_h = max_height / n
            tbl_h = n * row_h

        # Background rect
        canv.setFillColor(C_CARD)
        canv.rect(LM, y - tbl_h, UW, tbl_h, fill=1, stroke=0)
        canv.setStrokeColor(C_BORDER)
        canv.setLineWidth(0.4)
        canv.rect(LM, y - tbl_h, UW, tbl_h, fill=0, stroke=1)

        for i, (metric, score) in enumerate(breakdown.items()):
            row_y = y - (i + 1) * row_h
            mid   = row_y + row_h / 2

            # Row separator
            if i > 0:
                canv.setStrokeColor(C_BORDER)
                canv.setLineWidth(0.25)
                canv.line(LM + 4, row_y + row_h,
                          LM + UW - 4, row_y + row_h)

            # Label
            canv.setFont('Helvetica', 8.5)
            canv.setFillColor(C_MUTED)
            canv.drawString(LM + 0.3*cm, mid - 4, metric.title())

            # Bar track
            th = 9
            ty = mid - th / 2
            canv.setFillColor(C_BARBG)
            canv.roundRect(bar_x, ty, bar_w, th, 3, fill=1, stroke=0)

            # Bar fill
            fc = (C_GREEN if score >= 90 else
                  C_BLUE  if score >= 75 else
                  C_ORANGE if score >= 60 else C_RED)
            fw = max(0, score / 100 * bar_w)
            if fw > 0:
                canv.setFillColor(fc)
                canv.roundRect(bar_x, ty, fw, th, 3, fill=1, stroke=0)

            # Score text
            canv.setFont('Helvetica-Bold', 8.5)
            canv.setFillColor(C_WHITE)
            canv.drawString(score_x, mid - 4, f'{score}/100')

    canv.restoreState()


