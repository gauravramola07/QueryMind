# ── Page dimensions ───────────────────────────────────────────────────────────
PW, PH = A4           # 595.27 x 841.89 pts
LM = RM = 2.0 * cm   # left/right margin
TM = 2.5 * cm        # top margin
BM = 2.5 * cm        # bottom margin (increased for footer clearance)
UW = PW - LM - RM    # usable width ≈ 453 pts

# ── Colours ───────────────────────────────────────────────────────────────────
