def generate_pdf_report(df, fi, health, kpis, data_summary,
                         cleaning_report=None, figures=None,
                         file_name='dataset'):
    """Build and return QueryMind PDF as raw bytes."""

    buf      = io.BytesIO()
    styles   = _st()
    fi_name  = fi.get('file_name', file_name)

    # ── Build page callback closures ──────────────────────────────────────────
    on_first = _make_cover_drawer(fi, health)
    on_later = _make_page_drawer(fi_name)

    doc = SimpleDocTemplate(
        buf,
        pagesize=A4,
        leftMargin=LM, rightMargin=RM,
        topMargin=TM,  bottomMargin=BM,
        title='QueryMind Analytics Report',
        author='QueryMind',
    )

    story = []

    # ── PAGE 1: blank spacer (cover drawn by onFirstPage) ─────────────────────
    # We push a full-page spacer so Platypus emits page 1 and moves to page 2.
    # onFirstPage fires BEFORE Platypus draws anything, so the canvas is clear.
    story.append(Spacer(1, PH - TM - BM - 1))

    # ── PAGE 2: Statistics ────────────────────────────────────────────────────
    story.append(PageBreak())
    story.append(Paragraph('Dataset Statistics', styles['h2']))

    null_c = int(df.isnull().sum().sum())
    num_c  = int(df.select_dtypes(include=[np.number]).shape[1])
    mem_mb = round(df.memory_usage(deep=True).sum() / 1024**2, 2)

    story.append(_kpi_table([
        ('Total Rows',     f'{len(df):,}'),
        ('Total Columns',  len(df.columns)),
        ('Numeric Cols',   num_c),
        ('Missing Values', null_c),
        ('Memory',         f'{mem_mb} MB'),
    ], styles, UW))
    story.append(Spacer(1, 0.4*cm))

    ndf = df.select_dtypes(include=[np.number])
    if not ndf.empty:
        story.append(Paragraph('Numeric Summary', styles['h2']))
        desc = ndf.describe().round(2)
        abbr = {c: (c[:8]+'…' if len(c)>9 else c) for c in desc.columns}
        desc.columns = [abbr[c] for c in desc.columns]
        sw  = 2.2*cm
        cw_ = (UW - sw) / len(desc.columns)
        story.append(_data_table(
            ['Stat'] + list(desc.columns),
            [[str(i)] + [str(v) for v in r]
             for i, r in desc.iterrows()],
            UW, col_widths=[sw]+[cw_]*len(desc.columns), fsz=7,
        ))
        story.append(Spacer(1, 0.4*cm))

    if kpis:
        story.append(Paragraph('Key Metrics', styles['h2']))
        pairs = [(k['label'], k['formatted_value']) for k in kpis[:6]]
        for i in range(0, len(pairs), 3):
            story.append(_kpi_table(pairs[i:i+3], styles, UW))
            story.append(Spacer(1, 0.1*cm))

    # ── PAGE 3: AI Summary ────────────────────────────────────────────────────
    if data_summary:
        story.append(PageBreak())
        story.append(Paragraph('AI Executive Summary', styles['h2']))
        story.append(HRFlowable(width=UW, thickness=0.4,
                                color=C_BORDER, spaceAfter=8))
        clean = re.sub(r'\*\*(.+?)\*\*', r'\1', data_summary)
        clean = re.sub(r'#{1,6}\s*',     '',    clean)
        clean = re.sub(r'\n{3,}', '\n\n', clean).strip()
        for para in clean.split('\n\n'):
            if para.strip():
                story.append(Paragraph(para.strip(), styles['body']))
                story.append(Spacer(1, 0.1*cm))

    # ── PAGE 4: Visual Analytics ──────────────────────────────────────────────
    story.append(PageBreak())
    story.append(Paragraph('Visual Analytics', styles['h2']))
    story.append(Paragraph(
        'Automated charts generated from your dataset '
        'by the QueryMind analytics engine.',
        styles['muted']))
    story.append(Spacer(1, 0.2*cm))
    _add_charts(story, df, styles, figures=figures)

    # ── PAGE 5: Cleaning Report ───────────────────────────────────────────────
    if cleaning_report:
        cr = cleaning_report
        story.append(PageBreak())
        story.append(Paragraph('Smart Cleaning Report', styles['h2']))
        story.append(_kpi_table([
            ('Nulls Filled',       cr['total_nulls_filled']),
            ('Duplicates Removed', cr['duplicates_removed']),
            ('Columns Changed',    cr['columns_changed']),
            ('Rows After Clean',   f"{cr['rows_after']:,}"),
        ], styles, UW))
        story.append(Spacer(1, 0.35*cm))
        story.append(Paragraph('Per-Column Changes', styles['h2']))
        cr_rows = []
        for c in cr['columns']:
            ns = (f"{c['nulls_before']}→{c['nulls_after']} (−{c['nulls_filled']})"
                  if c['nulls_filled'] > 0 else str(c['nulls_before']))
            ts = (f"{c['dtype_before']}→{c['dtype_after']}"
                  if c['dtype_before'] != c['dtype_after']
                  else c['dtype_before'])
            us = (f"{c['unique_before']}→{c['unique_after']}"
                  if c['unique_before'] != c['unique_after']
                  else str(c['unique_before']))
            cr_rows.append([c['name'], ts, ns, us,
                            ' | '.join(c['actions']) or '—',
                            'Yes' if c['changed'] else '—'])
        story.append(_data_table(
            ['Column','Type','Nulls','Unique','Actions','Changed'],
            cr_rows, UW,
            col_widths=[3*cm,2.5*cm,2.5*cm,2*cm,4.5*cm,1.5*cm],
            fsz=7,
        ))

    # ── LAST PAGE: Column Schema ──────────────────────────────────────────────
    story.append(PageBreak())
    story.append(Paragraph('Column Schema', styles['h2']))
    story.append(_data_table(
        ['Column','Type','Non-Null','Nulls','Unique Values'],
        [[col, str(df[col].dtype),
          str(df[col].count()),
          str(int(df[col].isnull().sum())),
          str(df[col].nunique())]
         for col in df.columns],
        UW, col_widths=[4*cm,3*cm,3*cm,2.5*cm,3.5*cm],
    ))

    # ── Build ─────────────────────────────────────────────────────────────────
    doc.build(story,
              onFirstPage=on_first,
              onLaterPages=on_later)

    buf.seek(0)
    return buf.read()