def _grade_color(g):
    return {
        'A': C_GREEN, 'B': C_BLUE,
        'C': C_ORANGE, 'D': C_RED
    }.get(str(g).upper(), C_PURPLE)


def _fmt_num(v):
    try:
        a = abs(float(v))
        if a >= 1e6: return f'{v/1e6:.1f}M'
        if a >= 1e3: return f'{v/1e3:.1f}K'
        return f'{float(v):.1f}'
    except Exception:
        return str(v)


