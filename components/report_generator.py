def _styles():
    def s(n, **kw): return ParagraphStyle(n, **kw)
    return {
        'h2':   s('h2',   fontSize=13, textColor=C_PURPLT,
                   fontName='Helvetica-Bold',
                   spaceBefore=10, spaceAfter=6),
        'body': s('body', fontSize=9,  textColor=C_WHITE,
                   fontName='Helvetica', spaceAfter=5, leading=15),
        'muted':s('muted',fontSize=8,  textColor=C_MUTED,
                   fontName='Helvetica', spaceAfter=3),
        'kv':   s('kv',   fontSize=16, textColor=C_PURPLE,
                   fontName='Helvetica-Bold',
                   alignment=TA_CENTER, spaceAfter=2),
        'kl':   s('kl',   fontSize=7,  textColor=C_MUTED,
                   fontName='Helvetica', alignment=TA_CENTER),
        'cap':  s('cap',  fontSize=7,  textColor=C_MUTED,
                   fontName='Helvetica-Oblique',
                   alignment=TA_CENTER, spaceAfter=4),
    }


def _kpi_table(pairs, sty, width):
    n  = len(pairs)
    cw = [width / n] * n
    t  = Table(
        [[Paragraph(l.upper(), sty['kl']) for l, _ in pairs],
         [Paragraph(str(v),    sty['kv']) for _, v in pairs]],
        colWidths=cw,
    )
    t.setStyle(TableStyle([
        ('BACKGROUND',    (0,0),(-1,-1), C_CARD),
        ('BOX',           (0,0),(-1,-1), 0.4, C_BORDER),
        ('INNERGRID',     (0,0),(-1,-1), 0.4, C_BORDER),
        ('TOPPADDING',    (0,0),(-1,-1), 10),
        ('BOTTOMPADDING', (0,0),(-1,-1), 10),
        ('LEFTPADDING',   (0,0),(-1,-1), 8),
        ('RIGHTPADDING',  (0,0),(-1,-1), 8),
        ('VALIGN',        (0,0),(-1,-1), 'MIDDLE'),
    ]))
    return t


def _data_table(headers, rows, width, col_widths=None, fsz=8):
    if col_widths is None:
        col_widths = [width / len(headers)] * len(headers)
    TH = ParagraphStyle('TH', fontSize=fsz,   textColor=C_PURPLT,
                         fontName='Helvetica-Bold')
    TD = ParagraphStyle('TD', fontSize=fsz-1, textColor=C_WHITE,
                         fontName='Helvetica', leading=12)
    data = [[Paragraph(str(h), TH) for h in headers]]
    for r in rows:
        data.append([Paragraph(str(c), TD) for c in r])
    bgs = [('BACKGROUND', (0,i),(-1,i),
            C_CARD if i % 2 == 0 else C_ROW)
           for i in range(1, len(rows)+1)]
    t = Table(data, colWidths=col_widths, repeatRows=1)
    t.setStyle(TableStyle([
        ('BACKGROUND',    (0,0),(-1,0), C_HEAD),
        ('LINEBELOW',     (0,0),(-1,0), 0.5, C_PURPLE),
        ('BOX',           (0,0),(-1,-1), 0.4, C_BORDER),
        ('INNERGRID',     (0,0),(-1,-1), 0.3, C_BORDER),
        ('TOPPADDING',    (0,0),(-1,-1), 6),
        ('BOTTOMPADDING', (0,0),(-1,-1), 6),
        ('LEFTPADDING',   (0,0),(-1,-1), 8),
        ('RIGHTPADDING',  (0,0),(-1,-1), 4),
        ('VALIGN',        (0,0),(-1,-1), 'TOP'),
    ] + bgs))
    return t


