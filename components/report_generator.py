def _add_charts(story, df, styles, figures=None):
    nc = df.select_dtypes(include=[np.number]).columns.tolist()
    cc = df.select_dtypes(include=['object','category']).columns.tolist()

    items = []   # (flowable, caption)

    # 1. Plotly/kaleido
    if figures and _KALEIDO_AVAILABLE:
        for fig in figures[:4]:
            try:
                png = _pio.to_image(fig, format='png',
                                    width=860, height=380, scale=2)
                img = Image(io.BytesIO(png), width=UW, height=UW*380/860)
                items.append((img, ''))
            except Exception:
                pass

    # 2. matplotlib
    if len(items) < 4 and _MPL_AVAILABLE:
        specs = []
        if cc and nc:
            specs.append(('bar',  nc[0], cc[0], f'{nc[0]} by {cc[0]}'))
        if nc:
            specs.append(('hist', nc[0], None,  f'Distribution – {nc[0]}'))
        if len(nc) >= 2:
            specs.append(('hist', nc[1], None,  f'Distribution – {nc[1]}'))
        if len(cc) >= 2 and len(nc) >= 2:
            specs.append(('bar',  nc[1], cc[1], f'{nc[1]} by {cc[1]}'))
        for ct, c, g, ttl in specs[:4-len(items)]:
            img = _mpl_img(df, ttl, UW, ctype=ct, col=c, gc=g)
            if img: items.append((img, ttl))

    # 3. ReportLab native
    if len(items) < 4:
        specs = []
        if cc and nc:
            specs.append(('bar', nc[0], cc[0], f'{nc[0]} by {cc[0]}'))
        if cc and nc:
            specs.append(('pie', nc[0], cc[0], f'{cc[0]} – {nc[0]} share'))
        if len(nc) >= 2 and cc:
            specs.append(('bar', nc[1], cc[0], f'{nc[1]} by {cc[0]}'))
        if len(cc) >= 2 and len(nc) >= 2:
            specs.append(('pie', nc[1], cc[1], f'{cc[1]} – {nc[1]} share'))
        for ct, c, g, ttl in specs[:4-len(items)]:
            drw = (_bar_drawing(df, ttl, UW, col=c, group_col=g)
                   if ct == 'bar'
                   else _pie_drawing(df, ttl, UW, col=c, group_col=g))
            if drw is not None:
                items.append((drw, ttl))

    if not items:
        story.append(Paragraph(
            'No charts could be generated.', styles['muted']))
        return

    cap_s = ParagraphStyle('cap2', fontSize=7, textColor=C_MUTED,
                           fontName='Helvetica-Oblique',
                           alignment=TA_CENTER, spaceAfter=6)
    for fl, cap in items:
        story.append(fl)
        if cap:
            story.append(Paragraph(cap, cap_s))
        story.append(Spacer(1, 0.3*cm))


