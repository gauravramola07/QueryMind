def _draw_header_footer(canv, doc, fi_name):
    """Shared header/footer drawn on every page."""
    canv.saveState()

    # Background
    canv.setFillColor(C_BG)
    canv.rect(0, 0, PW, PH, fill=1, stroke=0)

    # Top purple bar
    canv.setFillColor(C_PURPLE)
    canv.rect(0, PH - 3, PW, 3, fill=1, stroke=0)

    # Header text
    canv.setFont('Helvetica', 7.5)
    canv.setFillColor(C_MUTED)
    canv.drawString(LM, PH - 1.4*cm, 'QueryMind Analytics Report')
    canv.drawRightString(PW - RM, PH - 1.4*cm, fi_name)

    # Header rule
    canv.setStrokeColor(C_BORDER)
    canv.setLineWidth(0.4)
    canv.line(LM, PH - 1.55*cm, PW - RM, PH - 1.55*cm)

    # Footer rule
    canv.line(LM, 1.3*cm, PW - RM, 1.3*cm)
    canv.setFont('Helvetica-Oblique', 7.5)
    ts = datetime.now().strftime('%B %d, %Y  %H:%M')
    canv.drawString(LM, 0.7*cm, f'Generated by QueryMind  |  {ts}')
    canv.drawRightString(PW - RM, 0.7*cm, f'Page {doc.page}')

    canv.restoreState()


def _make_cover_drawer(fi, health):
    """
    Returns an onFirstPage callback that draws the ENTIRE page 1
    content on the canvas — title, tagline, metadata, quality section.
    No Platypus paragraphs involved → zero overlap possible.
    """
    def _draw_cover(canv, doc):
        _draw_header_footer(canv, doc, fi.get('file_name', ''))
        canv.saveState()

        # ── Starting Y: just below the header rule ────────────────────────────
        y = PH - TM - 0.2*cm   # ~790 pts from bottom

        # ══ BRAND NAME ════════════════════════════════════════════════════════
        canv.setFont('Helvetica-Bold', 38)
        canv.setFillColor(C_WHITE)
        canv.drawString(LM, y, 'QueryMind')
        y -= 0.45*cm

        # ── Accent rule ───────────────────────────────────────────────────────
        canv.setStrokeColor(C_PURPLE)
        canv.setLineWidth(1.5)
        canv.line(LM, y, PW - RM, y)
        y -= 0.5*cm

        # ══ TAGLINE ═══════════════════════════════════════════════════════════
        canv.setFont('Helvetica', 14)
        canv.setFillColor(C_PURPLT)
        canv.drawString(LM, y, 'Data Analytics Report')
        y -= 0.9*cm

        # ══ METADATA BLOCK ════════════════════════════════════════════════════
        now  = datetime.now().strftime('%B %d, %Y  %H:%M')
        meta = [
            ('Dataset',   fi.get('file_name', '—')),
            ('Rows',      f"{fi.get('num_rows', 0):,}"),
            ('Columns',   str(fi.get('num_cols', 0))),
            ('Generated', now),
        ]
        lx = LM
        vx = LM + 2.2*cm
        for label, val in meta:
            canv.setFont('Helvetica-Bold', 9.5)
            canv.setFillColor(C_PURPLT)
            canv.drawString(lx, y, f'{label}:')
            canv.setFont('Helvetica', 9.5)
            canv.setFillColor(C_WHITE)
            canv.drawString(vx, y, val)
            y -= 0.52*cm

        y -= 0.5*cm  # gap before quality section

        # ══ DATA QUALITY HEADING ══════════════════════════════════════════════
        canv.setFont('Helvetica-Bold', 14)
        canv.setFillColor(C_PURPLT)
        canv.drawString(LM, y, 'Data Quality')
        y -= 0.55*cm

        # ══ GRADE PILL ════════════════════════════════════════════════════════
        grade     = str(health.get('grade', 'C')).upper()
        gc        = _grade_color(grade)
        grade_lbl = health.get('label', 'Fair')
        overall   = health.get('overall', 0)

        pill_h  = 1.8*cm
        pill_y  = y - pill_h

        # Pill card background
        canv.setFillColor(C_CARD)
        canv.roundRect(LM, pill_y, UW, pill_h, 5, fill=1, stroke=0)

        # Pill border in grade colour
        canv.setStrokeColor(gc)
        canv.setLineWidth(1.0)
        canv.roundRect(LM, pill_y, UW, pill_h, 5, fill=0, stroke=1)

        # ── Grade letter: centred in left 2.8cm ──────────────────────────────
        letter_w  = 2.8*cm
        letter_cx = LM + letter_w / 2
        # Vertical centre of pill
        pill_mid  = pill_y + pill_h / 2
        # For Helvetica-Bold at 42pt: cap-height ≈ 30pt, so centre offset = 15pt
        canv.setFont('Helvetica-Bold', 42)
        canv.setFillColor(gc)
        canv.drawCentredString(letter_cx, pill_mid - 15, grade)

        # Vertical divider
        canv.setStrokeColor(C_BORDER)
        canv.setLineWidth(0.5)
        div_x = LM + letter_w
        canv.line(div_x, pill_y + 8, div_x, pill_y + pill_h - 8)

        # ── Grade label + score: right of divider ─────────────────────────────
        tx = LM + letter_w + 0.45*cm
        # Grade label — upper 60% of pill
        canv.setFont('Helvetica-Bold', 15)
        canv.setFillColor(gc)
        canv.drawString(tx, pill_mid + 2, grade_lbl)
        # Score — lower 40% of pill
        canv.setFont('Helvetica', 9)
        canv.setFillColor(C_MUTED)
        canv.drawString(tx, pill_mid - 14, f'Overall Score: {overall}/100')

        y = pill_y - 0.3*cm

        # ══ METRIC PROGRESS BARS ══════════════════════════════════════════════
        breakdown = health.get('breakdown', {})
        if breakdown:
            bar_label_w = 3.0*cm
            bar_score_w = 2.2*cm
            bar_w       = UW - bar_label_w - bar_score_w
            bar_x       = LM + bar_label_w
            score_x     = bar_x + bar_w + 0.15*cm
            row_h       = 0.75*cm
            n_rows      = len(breakdown)
            table_h     = n_rows * row_h

            # Table background
            canv.setFillColor(C_CARD)
            canv.rect(LM, y - table_h, UW, table_h, fill=1, stroke=0)
            canv.setStrokeColor(C_BORDER)
            canv.setLineWidth(0.4)
            canv.rect(LM, y - table_h, UW, table_h, fill=0, stroke=1)

            for i, (metric, score) in enumerate(breakdown.items()):
                row_y = y - (i + 1) * row_h
                mid_y = row_y + row_h / 2

                # Row separator
                if i > 0:
                    canv.setStrokeColor(C_BORDER)
                    canv.setLineWidth(0.25)
                    canv.line(LM + 4, row_y + row_h,
                              LM + UW - 4, row_y + row_h)

                # Metric label
                canv.setFont('Helvetica', 8.5)
                canv.setFillColor(C_MUTED)
                canv.drawString(LM + 0.3*cm, mid_y - 4, metric.title())

                # Bar track
                track_h = 9
                track_y = mid_y - track_h / 2
                canv.setFillColor(C_BAR_BG)
                canv.roundRect(bar_x, track_y, bar_w, track_h,
                               3, fill=1, stroke=0)

                # Bar fill
                fc = (C_GREEN if score >= 90 else
                      C_BLUE  if score >= 75 else
                      C_ORANGE if score >= 60 else C_RED)
                fw = max(0, score / 100 * bar_w)
                if fw > 0:
                    canv.setFillColor(fc)
                    canv.roundRect(bar_x, track_y, fw, track_h,
                                   3, fill=1, stroke=0)

                # Score label
                canv.setFont('Helvetica-Bold', 8.5)
                canv.setFillColor(C_WHITE)
                canv.drawString(score_x, mid_y - 4, f'{score}/100')

        canv.restoreState()

    return _draw_cover


def _make_page_drawer(fi_name):
    """Returns an onLaterPages callback (header/footer only)."""
    def _draw(canv, doc):
        _draw_header_footer(canv, doc, fi_name)
    return _draw


