def _bar(df, title, w, col=None, gcol=None):
    try:
        nc = df.select_dtypes(include=[np.number]).columns.tolist()
        cc = df.select_dtypes(include=['object','category']).columns.tolist()
        if not nc: return None
        if not col or col not in df.columns: col = nc[0]
        if not gcol and cc: gcol = cc[0]
        if gcol and gcol in df.columns:
            g = df.groupby(gcol)[col].sum().nlargest(8)
            labels = [str(x)[:12] for x in g.index]
            vals   = [float(v) for v in g.values]
        else:
            cnt, edges = np.histogram(df[col].dropna(), bins=8)
            labels = [f'{e:.0f}' for e in edges[:-1]]
            vals   = [float(c) for c in cnt]
        if not vals or max(vals) == 0: return None

        cw = float(w); ch = 185
        pl=55; pb=32; pt=26; pr=6
        pw = cw-pl-pr; ph = ch-pb-pt
        d = Drawing(cw, ch)
        d.add(Rect(0,0,cw,ch, fillColor=C_CARD,
                   strokeColor=C_BORDER, strokeWidth=0.5))
        mx = max(vals); n = len(vals)
        sw = pw/n; bw = sw*0.65; gap=(sw-bw)/2
        for i in range(5):
            gy = pb+ph*i/4
            d.add(Line(pl,gy,cw-pr,gy,
                       strokeColor=C_BORDER,strokeWidth=0.3))
            d.add(String(pl-3,gy-3,_fmt(mx*i/4),
                         fontSize=5.5,fillColor=C_MUTED,textAnchor='end'))
        for i,(lbl,val) in enumerate(zip(labels,vals)):
            bx = pl+i*sw+gap; bh=max(2,val/mx*ph)
            clr = PALETTE[i%len(PALETTE)]
            d.add(Rect(bx,pb,bw,bh,fillColor=clr,strokeColor=None))
            d.add(String(bx+bw/2,pb+bh+2,_fmt(val),
                         fontSize=5,fillColor=C_WHITE,textAnchor='middle'))
            d.add(String(bx+bw/2,pb-12,lbl[:10],
                         fontSize=5.5,fillColor=C_MUTED,textAnchor='middle'))
        d.add(String(cw/2,ch-14,title,fontSize=8.5,fillColor=C_WHITE,
                     fontName='Helvetica-Bold',textAnchor='middle'))
        return d
    except Exception:
        print(f'[PDF bar] {traceback.format_exc()}')
        return None


def _pie(df, title, w, col=None, gcol=None):
    try:
        nc = df.select_dtypes(include=[np.number]).columns.tolist()
        cc = df.select_dtypes(include=['object','category']).columns.tolist()
        if not nc or not cc: return None
        if not col or col not in df.columns: col = nc[0]
        if not gcol: gcol = cc[0]
        if gcol not in df.columns: return None
        g = df.groupby(gcol)[col].sum().nlargest(6)
        if g.empty: return None
        labels=[str(x)[:16] for x in g.index]
        vals=[float(v) for v in g.values]
        total=sum(vals)
        cw=float(w); ch=185
        d=Drawing(cw,ch)
        d.add(Rect(0,0,cw,ch,fillColor=C_CARD,
                   strokeColor=C_BORDER,strokeWidth=0.5))
        d.add(String(cw/2,ch-14,title,fontSize=8.5,fillColor=C_WHITE,
                     fontName='Helvetica-Bold',textAnchor='middle'))
        p=Pie()
        p.x=30; p.y=22; p.width=120; p.height=120
        p.data=vals
        p.labels=[f'{v/total*100:.1f}%' for v in vals]
        p.sideLabels=True; p.sideLabelsOffset=0.08; p.simpleLabels=False
        for i in range(len(vals)):
            p.slices[i].fillColor=PALETTE[i%len(PALETTE)]
            p.slices[i].strokeColor=C_BG; p.slices[i].strokeWidth=0.5
            p.slices[i].fontSize=5.5; p.slices[i].fontColor=C_WHITE
        d.add(p)
        lx=188
        for i,(lbl,val) in enumerate(zip(labels,vals)):
            ly=ch-36-i*20
            if ly<12: break
            d.add(Rect(lx,ly,9,9,
                       fillColor=PALETTE[i%len(PALETTE)],strokeColor=None))
            d.add(String(lx+13,ly+1,f'{lbl}: {_fmt(val)}',
                         fontSize=6.5,fillColor=C_WHITE,textAnchor='start'))
        return d
    except Exception:
        print(f'[PDF pie] {traceback.format_exc()}')
        return None


def _mpl(df, title, w, ctype='bar', col=None, gcol=None):
    if not _MPL_AVAILABLE: return None
    try:
        nc=df.select_dtypes(include=[np.number]).columns.tolist()
        if not nc: return None
        if not col or col not in df.columns: col=nc[0]
        cc=df.select_dtypes(include=['object','category']).columns.tolist()
        if not gcol and cc: gcol=cc[0]
        fig,ax=plt.subplots(figsize=(8,3),facecolor='#0d0d2b')
        ax.set_facecolor('#1a1a3e')
        if ctype=='bar' and gcol and gcol in df.columns:
            g=df.groupby(gcol)[col].sum().nlargest(10)
            ax.bar(g.index.astype(str),g.values,
                   color='#667eea',edgecolor='#2d3748',lw=0.4)
        else:
            ax.hist(df[col].dropna(),bins=20,
                    color='#667eea',edgecolor='#2d3748',lw=0.4)
        ax.set_title(title,color='#f7fafc',fontsize=9,pad=5)
        ax.tick_params(colors='#a0aec0',labelsize=7)
        for sp in ax.spines.values(): sp.set_edgecolor('#2d3748')
        plt.xticks(rotation=25,ha='right'); plt.tight_layout(pad=0.3)
        buf=io.BytesIO()
        fig.savefig(buf,format='png',dpi=130,
                    facecolor='#0d0d2b',bbox_inches='tight')
        plt.close(fig); buf.seek(0)
        return Image(buf,width=w,height=w*3/8)
    except Exception:
        print(f'[PDF mpl] {traceback.format_exc()}')
        return None


def _add_charts(story, df, sty, figures=None):
    nc=df.select_dtypes(include=[np.number]).columns.tolist()
    cc=df.select_dtypes(include=['object','category']).columns.tolist()
    items=[]

    if figures and _KALEIDO_AVAILABLE:
        for fig in figures[:4]:
            try:
                png=_pio.to_image(fig,format='png',width=860,height=360,scale=2)
                items.append((Image(io.BytesIO(png),
                                    width=UW,height=UW*360/860),''))
            except Exception:
                pass

    if len(items)<4 and _MPL_AVAILABLE:
        specs=[]
        if cc and nc: specs.append(('bar',nc[0],cc[0],f'{nc[0]} by {cc[0]}'))
        if nc:        specs.append(('hist',nc[0],None,f'Distribution – {nc[0]}'))
        if len(nc)>=2:specs.append(('hist',nc[1],None,f'Distribution – {nc[1]}'))
        if len(cc)>=2 and len(nc)>=2:
            specs.append(('bar',nc[1],cc[1],f'{nc[1]} by {cc[1]}'))
        for ct,c,g,ttl in specs[:4-len(items)]:
            img=_mpl(df,ttl,UW,ctype=ct,col=c,gcol=g)
            if img: items.append((img,ttl))

    if len(items)<4:
        specs=[]
        if cc and nc:
            specs.append(('bar',nc[0],cc[0],f'{nc[0]} by {cc[0]}'))
            specs.append(('pie',nc[0],cc[0],f'{cc[0]} share of {nc[0]}'))
        if len(nc)>=2 and cc:
            specs.append(('bar',nc[1],cc[0],f'{nc[1]} by {cc[0]}'))
        if len(cc)>=2 and len(nc)>=2:
            specs.append(('pie',nc[1],cc[1],f'{cc[1]} share of {nc[1]}'))
        for ct,c,g,ttl in specs[:4-len(items)]:
            drw=(_bar(df,ttl,UW,col=c,gcol=g) if ct=='bar'
                 else _pie(df,ttl,UW,col=c,gcol=g))
            if drw is not None: items.append((drw,ttl))

    if not items:
        story.append(Paragraph('No charts could be generated.',sty['muted']))
        return

    cap_s=ParagraphStyle('cap2',fontSize=7,textColor=C_MUTED,
                          fontName='Helvetica-Oblique',
                          alignment=TA_CENTER,spaceAfter=5)
    for fl,cap in items:
        story.append(fl)
        if cap: story.append(Paragraph(cap,cap_s))
        story.append(Spacer(1,0.25*cm))


