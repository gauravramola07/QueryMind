def _bar_drawing(df, title, w, col=None, group_col=None):
    try:
        nc = df.select_dtypes(include=[np.number]).columns.tolist()
        cc = df.select_dtypes(include=['object','category']).columns.tolist()
        if not nc: return None
        if not col or col not in df.columns: col = nc[0]
        if not group_col and cc: group_col = cc[0]

        if group_col and group_col in df.columns:
            g = df.groupby(group_col)[col].sum().nlargest(8)
            labels = [str(x)[:12] for x in g.index]
            vals   = [float(v) for v in g.values]
        else:
            cnt, edges = np.histogram(df[col].dropna(), bins=8)
            labels = [f'{e:.0f}' for e in edges[:-1]]
            vals   = [float(c) for c in cnt]

        if not vals or max(vals) == 0: return None

        cw_ = float(w); ch = 185
        pl = 55; pb = 32; pt = 26; pr = 6
        pw_ = cw_ - pl - pr; ph = ch - pb - pt

        d = Drawing(cw_, ch)
        d.add(Rect(0,0,cw_,ch, fillColor=C_CARD,
                   strokeColor=C_BORDER, strokeWidth=0.5))

        mx = max(vals); n = len(vals)
        sw_ = pw_/n; bw_ = sw_*0.65; gap = (sw_-bw_)/2

        for i in range(5):
            gy = pb + ph*i/4
            d.add(Line(pl,gy,cw_-pr,gy,
                       strokeColor=C_BORDER, strokeWidth=0.3))
            d.add(String(pl-3, gy-3, _fmt_num(mx*i/4),
                         fontSize=5.5, fillColor=C_MUTED,
                         textAnchor='end'))

        for i,(lbl,val) in enumerate(zip(labels,vals)):
            bx  = pl + i*sw_ + gap
            bh_ = max(2, val/mx*ph)
            clr = PALETTE[i % len(PALETTE)]
            d.add(Rect(bx,pb,bw_,bh_, fillColor=clr, strokeColor=None))
            d.add(String(bx+bw_/2, pb+bh_+2, _fmt_num(val),
                         fontSize=5, fillColor=C_WHITE, textAnchor='middle'))
            d.add(String(bx+bw_/2, pb-12, lbl[:10],
                         fontSize=5.5, fillColor=C_MUTED, textAnchor='middle'))

        d.add(String(cw_/2, ch-14, title, fontSize=8.5, fillColor=C_WHITE,
                     fontName='Helvetica-Bold', textAnchor='middle'))
        return d
    except Exception:
        print(f'[PDF] bar "{title}":\n{traceback.format_exc()}')
        return None


def _pie_drawing(df, title, w, col=None, group_col=None):
    try:
        nc = df.select_dtypes(include=[np.number]).columns.tolist()
        cc = df.select_dtypes(include=['object','category']).columns.tolist()
        if not nc or not cc: return None
        if not col or col not in df.columns: col = nc[0]
        if not group_col: group_col = cc[0]
        if group_col not in df.columns: return None

        g = df.groupby(group_col)[col].sum().nlargest(6)
        if g.empty: return None
        labels = [str(x)[:16] for x in g.index]
        vals   = [float(v) for v in g.values]
        total  = sum(vals)

        cw_ = float(w); ch = 185
        d = Drawing(cw_, ch)
        d.add(Rect(0,0,cw_,ch, fillColor=C_CARD,
                   strokeColor=C_BORDER, strokeWidth=0.5))
        d.add(String(cw_/2, ch-14, title, fontSize=8.5, fillColor=C_WHITE,
                     fontName='Helvetica-Bold', textAnchor='middle'))

        pie = Pie()
        pie.x=30; pie.y=22; pie.width=120; pie.height=120
        pie.data   = vals
        pie.labels = [f'{v/total*100:.1f}%' for v in vals]
        pie.sideLabels=True; pie.sideLabelsOffset=0.08
        pie.simpleLabels=False
        for i in range(len(vals)):
            pie.slices[i].fillColor   = PALETTE[i%len(PALETTE)]
            pie.slices[i].strokeColor = C_BG
            pie.slices[i].strokeWidth = 0.5
            pie.slices[i].fontSize    = 5.5
            pie.slices[i].fontColor   = C_WHITE
        d.add(pie)

        lx = 188
        for i,(lbl,val) in enumerate(zip(labels,vals)):
            ly = ch-36-i*20
            if ly < 12: break
            d.add(Rect(lx,ly,9,9,
                       fillColor=PALETTE[i%len(PALETTE)],
                       strokeColor=None))
            d.add(String(lx+13, ly+1, f'{lbl}: {_fmt_num(val)}',
                         fontSize=6.5, fillColor=C_WHITE, textAnchor='start'))
        return d
    except Exception:
        print(f'[PDF] pie "{title}":\n{traceback.format_exc()}')
        return None


def _mpl_img(df, title, w, ctype='bar', col=None, gc=None):
    if not _MPL_AVAILABLE: return None
    try:
        nc = df.select_dtypes(include=[np.number]).columns.tolist()
        if not nc: return None
        if not col or col not in df.columns: col = nc[0]
        cc = df.select_dtypes(include=['object','category']).columns.tolist()
        if not gc and cc: gc = cc[0]

        fig, ax = plt.subplots(figsize=(8, 3), facecolor='#0d0d2b')
        ax.set_facecolor('#1a1a3e')
        if ctype == 'bar' and gc and gc in df.columns:
            g = df.groupby(gc)[col].sum().nlargest(10)
            ax.bar(g.index.astype(str), g.values,
                   color='#667eea', edgecolor='#2d3748', lw=0.4)
        else:
            ax.hist(df[col].dropna(), bins=20,
                    color='#667eea', edgecolor='#2d3748', lw=0.4)
        ax.set_title(title, color='#f7fafc', fontsize=9, pad=5)
        ax.tick_params(colors='#a0aec0', labelsize=7)
        for sp in ax.spines.values(): sp.set_edgecolor('#2d3748')
        plt.xticks(rotation=25, ha='right')
        plt.tight_layout(pad=0.3)
        buf = io.BytesIO()
        fig.savefig(buf, format='png', dpi=130,
                    facecolor='#0d0d2b', bbox_inches='tight')
        plt.close(fig)
        buf.seek(0)
        return Image(buf, width=w, height=w*3/8)
    except Exception:
        print(f'[PDF] mpl "{title}":\n{traceback.format_exc()}')
        return None


